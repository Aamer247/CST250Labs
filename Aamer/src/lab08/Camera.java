package lab08;

public class Camera {
	String address;
	String firstApproach;
	String secondApproach;
	String goLiveDate;
	double latitude;
	double longitude;
	String location;
	
	public Camera(String address, String firstApproach, String secondApproach, String goLiveDate, double latitude,
			double longitude, String location) {
		super();
		this.address = address;
		this.firstApproach = firstApproach;
		this.secondApproach = secondApproach;
		this.goLiveDate = goLiveDate;
		this.latitude = latitude;
		this.longitude = longitude;
		this.location = location;
	}
	
	

}
